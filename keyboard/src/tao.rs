#[cfg(test)]
mod tests {
    use strum::IntoEnumIterator as _;

    #[test]
    fn round_trip() {
        for key in crate::Key::iter() {
            let keycode: tao::keyboard::KeyCode = key.into();
            let round_trip_key: crate::Key = keycode.try_into().unwrap();
            assert_eq!(key, round_trip_key);
        }
    }
}
